package telnet;



public class Beispiel {

	
	public void printString(String str){
		System.out.println("printString aufgerufen: " + str);
	}
	
	public void printStrings(String str1, String str2){
		System.out.println("printStrings aufgerufen: "  + str1 + " " + str2);
	}
	
	public void printInteger(int i){
		System.out.println("printInteger aufgerufen: " + i);
	}
	
	public void printBoolean(boolean bool){
		System.out.println("printBoolean aufgerufen: " + bool);
	}
}
